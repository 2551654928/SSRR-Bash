# 安装 依赖
Installation_dependency(){
	if [[ ${release} == "centos" ]]; then
		Centos_yum
	else
		Debian_apt
	fi
	[[ ! -e "/usr/bin/unzip" ]] && echo -e "${Error} 依赖 unzip(解压压缩包) 安装失败，多半是软件包源的问题，请检查 !" && exit 1
	Check_python
	#echo "nameserver 8.8.8.8" > /etc/resolv.conf
	#echo "nameserver 1.1.1.1" >> /etc/resolv.conf
	\cp -f /usr/share/zoneinfo/Asia/Shanghai /etc/localtime
	if [[ ${release} == "centos" ]]; then
		/etc/init.d/crond restart
	else
		/etc/init.d/cron restart
	fi
}
# 下载 ShadowsocksR
Download_SSR(){
	cd "/usr/local"
	echo -e "${Info} 开始下载SSR"
	wget -N --no-check-certificate "https://github.com/Chennhaoo/SSPanel-SSR/raw/master/shadowsocks-sspanel.zip"
	[[ ! -e "shadowsocks-sspanel.zip" ]] && echo -e "${Error} ShadowsocksR服务端 压缩包 下载失败 !" && rm -rf shadowsocks-sspanel.zip && exit 1
	unzip "shadowsocks-sspanel.zip"
	[[ ! -e "/usr/local/shadowsocks-sspanel/" ]] && echo -e "${Error} ShadowsocksR服务端 解压失败 !" && rm -rf shadowsocks-sspanel.zip && exit 1
	mv "/usr/local/shadowsocks-sspanel/" "/usr/local/shadowsocksr/"
	[[ ! -e "/usr/local/shadowsocksr/" ]] && echo -e "${Error} ShadowsocksR服务端 重命名失败 !" && rm -rf shadowsocks-sspanel.zip &&  rm -rf "/usr/local/shadowsocks-sspanel/" && exit 1	
	rm -rf shadowsocks-sspanel.zip
	cd "shadowsocksr"
	echo -e "${Info} PIP插件安装"
	if [[ ${release} = "centos" ]]; then
		yum install epel-release -y && yum makecache
		yum install python-pip -y
		pip install --upgrade pip
		pip install -r requirements.txt
	else
		apt install python-setuptools python-pip build-essential -y
		python -m pip install --upgrade pip
		python -m pip install -r requirements.txt		
	fi
	echo -e "${Info} ShadowsocksR服务端 下载完成 !"
}

